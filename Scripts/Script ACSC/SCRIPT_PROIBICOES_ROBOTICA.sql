SELECT 
C.CD_CONVENIO||' - '||C.NM_CONVENIO,
P.CD_CON_PLA,
P.CD_PRO_FAT||' - '||PF.DS_PRO_FAT COD_PROCED, 
P.TP_PROIBICAO,
P.TP_ATENDIMENTO,
P.DT_INICIAL_PROIBICAO,
P.CD_MULTI_EMPRESA
FROM PROIBICAO P
INNER JOIN CONVENIO C ON C.CD_CONVENIO = P.CD_CONVENIO
INNER JOIN PRO_FAT PF ON PF.CD_PRO_FAT = P.CD_PRO_FAT
WHERE P.CD_MULTI_EMPRESA = 3 
AND P.CD_PRO_FAT IN (
SELECT CD_PRO_FAT FROM PRO_FAT WHERE CD_PRO_FAT IN ('02037623',
'02037627',
'02037628',
'02037629',
'02037632',
'02037637',
'03000006'))
ORDER BY P.CD_CONVENIO, P.CD_CON_PLA, P.CD_PRO_FAT
