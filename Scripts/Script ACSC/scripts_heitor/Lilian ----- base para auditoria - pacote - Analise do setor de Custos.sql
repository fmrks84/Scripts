SELECT E.CD_CONVENIO,
 C.NM_CONVENIO,
 P.CD_PRO_FAT,
 P.DS_PRO_FAT,
 E.CD_PRO_FAT_PACOTE,
 F2.DS_PRO_FAT DS_PRO_FAT_PACOTE,
 E.DT_VIGENCIA,
 E.DT_VIGENCIA_FINAL

FROM PACOTE E
INNER JOIN PRO_FAT P ON (E.CD_PRO_FAT = P.CD_PRO_FAT)
INNER JOIN PRO_FAT F2 ON (E.CD_PRO_FAT_PACOTE = F2.CD_PRO_FAT)
INNER JOIN CONVENIO C ON (C.CD_CONVENIO = E.CD_CONVENIO)

WHERE E.CD_CONVENIO = '40'
AND to_date(E.DT_VIGENCIA_FINAL,'dd/mm/rrrr') > to_date('20/09/2021' , 'dd/mm/rrrr')
--AND E.CD_PRO_FAT_PACOTE = '10000430'
ORDER BY 3 

;

