g.cd_guia,
g.tp_guia,
gg.cd_pro_fat,
gg.ds_procedimento,
gg.sn_fatura_direto
from 
aviso_cirurgia cr 
inner join guia g on g.cd_aviso_cirurgia = cr.cd_aviso_cirurgia and g.tp_guia = 'O'
inner join it_guia gg on gg.cd_guia = g.cd_guia
where cr.cd_aviso_cirurgia in (/*348467*/348996)
;
