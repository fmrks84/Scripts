
SELECT  DT_VIGENCIA,CD_PRO_FAT,VL_HONORARIO,VL_OPERACIONAL,VL_TOTAL,CD_TAB_FAT,SN_ATIVO 
FROM    DBAMV.VAL_PRO 
WHERE   VAL_PRO.CD_PRO_FAT IN 
(SELECT PRO_FAT.CD_PRO_FAT 
 FROM   DBAMV.PRO_FAT 
 WHERE  CD_SUS IS NULL) 
 AND   (CD_TAB_FAT IN (279))
 --AND   SN_ATIVO = 'N'
 AND    CD_PRO_FAT IN ('00017123','00017124','00017149','00017150','00017151'
                  ,'00017152','00017153','00017154','00017157','00017158'
                  ,'00017161','00017162','00017711','00017786')
ORDER BY DT_VIGENCIA, CD_PRO_FAT
