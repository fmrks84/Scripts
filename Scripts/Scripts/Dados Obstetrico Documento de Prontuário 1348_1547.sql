SELECT PACIENTE.CD_PACIENTE, PACIENTE.NM_PACIENTE,                              ---PACIENTE
       ATENDIME.CD_ATENDIMENTO, ATENDIME.TP_ATENDIMENTO,                        ---ATENDIMENTO
       PERGUNTA_DOC.DS_PERGUNTA, REGISTRO_DOCUMENTO.CD_REGISTRO_DOCUMENTO,      ---DOCUMENTO
       REGISTRO_RESPOSTA.DS_RESPOSTA, REGISTRO_RESPOSTA.CD_PERGUNTA_DOC         -- RESPOSTAS                                           
       
FROM   DBAMV.REGISTRO_RESPOSTA, DBAMV.REGISTRO_DOCUMENTO, DBAMV.ATENDIME, DBAMV.PACIENTE, DBAMV.PERGUNTA_DOC
WHERE  REGISTRO_DOCUMENTO.CD_DOCUMENTO        IN (1348,1547)
AND    REGISTRO_RESPOSTA.CD_PERGUNTA_DOC      IN (8469,8470,8471,8465,8466,8290,8467,415  ,8481,8468,8483,8484,8476,8477,8478
                                                 ,8488,8487,8490,8493,8489,9113,7513,10592,6914,6915,6916,6910,6911,6917,6912
                                                 ,6913,7150,7109,7110,7580,7581,7287,7297,7117,7153,7512,7152,7116,7113,7283,10255)
AND    REGISTRO_RESPOSTA.CD_REGISTRO_DOCUMENTO = REGISTRO_DOCUMENTO.CD_REGISTRO_DOCUMENTO
AND    REGISTRO_RESPOSTA.CD_PERGUNTA_DOC       = PERGUNTA_DOC.CD_PERGUNTA_DOC
AND    REGISTRO_DOCUMENTO.CD_ATENDIMENTO       = ATENDIME.CD_ATENDIMENTO
AND    ATENDIME.CD_PACIENTE                    = PACIENTE.CD_PACIENTE                                            
--AND    ATENDIME.CD_ATENDIMENTO                 IN (542594,542556)
ORDER BY REGISTRO_RESPOSTA.CD_PERGUNTA_DOC, ATENDIME.CD_ATENDIMENTO 
